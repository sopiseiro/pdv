    public void cancelaVenda(){
        bd b = new bd();
        b.connect();
        mySql f = new mySql(b.conn);
        mySql f1 = new mySql(b.conn);
        String modeloAux = "";
        String serieAux = "";
        
        if (Main.modelo.length() == 1){
            if (Character.isLetter(Main.modelo.charAt(0)))
                modeloAux = "modelo ='"+Main.modelo+"'";
            else
                modeloAux = "modelo ="+Main.modelo+"";
        }else{
            modeloAux = "modelo ='"+Main.modelo+"'";
        }
        
        if (Main.serie.length() == 1 ){
            if (Character.isLetter(Main.serie.charAt(0)))
                serieAux = "serie ='"+Main.serie+"'";
            else
                serieAux = "serie ="+Main.serie+"";
        }else{
            serieAux = "serie ='"+Main.serie+"'";
        }
        
        f.open("SELECT NOTA FROM VENDAS where cancelada = 0 and "+modeloAux+" and "+ serieAux +" ORDER BY NUMERO DESC");
        
        while (f.next()){
            codigo = f.fieldbyname("NOTA");
        }
        System.out.println(codigo+"asdf ");
        
        f.execute("UPDATE Itevendas SET cancelada = 1 WHERE nota = '" + codigo + "'");
        f.execute("UPDATE vendas SET cancelada = 1 WHERE nota = '" + codigo + "'");
        
        f.open("SELECT qtd, codigo FROM Itevendas WHERE nota = '"+codigo+"'");
        while (f.next()){
            System.out.println("UPDATE ESTOQUE SET QTD = QTD + " + f.fieldbyname("qtd") + " WHERE CODIGO = '" + f.fieldbyname("codigo") + "'");
            f1.execute("UPDATE ESTOQUE SET QTD = QTD + " + Double.parseDouble(f.fieldbyname("qtd").replace(",", ".")) + " WHERE CODIGO = '" + f.fieldbyname("codigo") + "'");
        }
            
    }

